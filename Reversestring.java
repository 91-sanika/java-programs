package com.edu.loops;

public class Reversestring {

	public static void main(String[] args) {
	    int r = 0;
	    for (int i= 0; i<= r-1 ; i++)
	    {
	        for (int j=0; j<=i; j++)
	        {
	            System.out.print("Sanika");
	        }
	        for (int k=0; k<=r-1-i; k++)
	        {
	            System.out.print("*" + " Sanika");
	        }
	        System.out.println();
	    }
	}
}
