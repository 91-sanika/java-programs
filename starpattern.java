package com.edu.loops;

public class starpattern {

	public static void main(String[] args) {
		
		for(int i=1;i<=3;i++) {  //i=1
			for(int j=1;j<=2;j++) { //j =1 to 2
				System.out.println("i="+i+" and j="+j);  //i=1  j=1  j=2
				                                          //i=2 j=1  j=2
				                                            //i=3 j=1 j=2
			}
		}

	}

}
